package activity5_forloops;

public class ForLoops
{

	public static void main(String[] args)
	{
		
		// For-Loop to count from 1 to 10 (start the int at 1, iterate until we reach 11, then stop)
		for (int i=1; i<11; i++)
		{
			// Output the current iteration (count!)
			System.out.println("Count: " + i);
		}

	}

}
